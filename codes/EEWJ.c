#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#define MAX_TOKENS 512
#define MAX_TOKEN_LENGTH 100

int main() {
    char input[MAX_TOKENS][MAX_TOKEN_LENGTH];
    int num_tokens = 0;

    // Read input from user
    printf("Enter tokens (one per line, max %d tokens):\n", MAX_TOKENS);
    while (num_tokens < MAX_TOKENS && fgets(input[num_tokens], MAX_TOKEN_LENGTH, stdin) != NULL) {
        // Remove newline character from input
        input[num_tokens][strcspn(input[num_tokens], "\n")] = '\0';
        num_tokens++;
    }

    // Print input tokens
    printf("Input tokens:\n");
    for (int i = 0; i < num_tokens; i++) {
        printf("%s\n", input[i]);
    }

    return 0;
}

































































































































































































































